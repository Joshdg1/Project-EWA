class UserDate{
    title
    start
    end
    hoursPerDayStart
    hoursPerDayEnd

    constructor(title, start, end, hoursPerDayStart, hoursPerDayEnd) {
        this.title = title;
        this.start = start;
        this.end = end;
        this.hoursPerDayStart = hoursPerDayStart;
        this.hoursPerDayEnd = hoursPerDayEnd;
    }
}
export default UserDate